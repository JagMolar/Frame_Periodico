    private void botonCuentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCuentasActionPerformed
        
        int premium=0;
        for(int i = 0; i < usuariosList.size() ; i++){
            if(usuariosList.get(i).isPremium()){
                premium ++;
            }
        }
        JOptionPane.showMessageDialog(this,"El total de premium es " + premium + " usuarios.", "Premium",JOptionPane.INFORMATION_MESSAGE);
        
        int normal = usuariosList.size()-premium;
        JOptionPane.showMessageDialog(this,"El total de normales es " + normal + " usuarios.", "Normal",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_botonCuentasActionPerformed

