    private void botonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirActionPerformed
        // Creamos un objeto Usuario. 
        
        Usuario user= new Usuario();
        boolean correcto =true;
        
        //Ahora, comprobamos la contraseña y de no ser correcta, el error.
        Pattern mail = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        
        Matcher mCorreo = mail.matcher(textoUsuario.getText());
        
        if(mCorreo.matches()){
            user.setEmail(textoUsuario.getText());
        }else{
            JOptionPane.showMessageDialog(this,"Formato incorrecto", "Error",JOptionPane.ERROR_MESSAGE);
            correcto= false;
        }
       
        user.setContrasena(textoContrasena.getText());
        
        try {
            user.setImporte(Double.parseDouble(textoDescuento.getText()));
        } catch (NumberFormatException e) { 
            JOptionPane.showMessageDialog(this,"Error en el importe", "error", JOptionPane.ERROR_MESSAGE);
            correcto = false;
        }
        
        if (premium.isSelected()) {
            user.setPremium(true);
            
        } else {
            user.setPremium(false);
        }
        
        if(correcto){
            usuariosList.add(user);
            JOptionPane.showMessageDialog(this,"Usuario añadido", "Añadido", JOptionPane.INFORMATION_MESSAGE);
            
        }else {
            JOptionPane.showMessageDialog(this,"No se añadió al usuario", "error", JOptionPane.ERROR_MESSAGE);
        }
        verLista();
    }//GEN-LAST:event_botonAnadirActionPerformed

