    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // La búsqueda es por el identificador (mail)
        Usuario user = new Usuario();
        boolean encontrado = false;
        
        String email;
        email = textoUsuario.getText();
        
        if (!usuariosList.isEmpty()) {
            for(int i = 0; i<usuariosList.size()&&!encontrado;i++){  
                user = usuariosList.get(i);
                if(email.equalsIgnoreCase(user.getEmail())){
                    encontrado = true;
                }
            }
            
            if(encontrado){
                textoUsuario.setText(user.getEmail());
                textoContrasena.setText(user.getContrasena());
                textoDescuento.setText(user.getImporte()+"");
                if(user.isPremium()){
                    premium.setSelected(true);
                    JOptionPane.showMessageDialog(this,"El usuario que busca es " + email , ".",JOptionPane.INFORMATION_MESSAGE);
                }else{
                    noPremium.setSelected(true);
                    JOptionPane.showMessageDialog(this,"El usuario que busca es " + email , ".",JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this,"No existen coincidencias", "No encontrado", JOptionPane.ERROR_MESSAGE);
            }   
        } else {
            JOptionPane.showMessageDialog(this,"No hay usuarios", "Lista vacía", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonIngresosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresosActionPerformed
        
        double total=0;
             
       for(int i = 0 ; i < this.usuariosList.size() ; i++){
           if(usuariosList.get(i).isPremium()){
               total += 35.5;
           }else{
               total += 20.5;
           }
           
           total -= usuariosList.get(i).getImporte();
       }
        JOptionPane.showMessageDialog(this,"El total de ingresos es " + total + " euros.", "Ingresos",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_botonIngresosActionPerformed

